def generate_enemy_line_prompt(excuse_text: str, attempt_num: int) -> str:
    """
    배틀 중 적의 대사 생성

    Args:
        excuse_text: 선택된 변명
        attempt_num: 현재 턴 (1-3)

    Returns:
        프롬프팅 문자열
    """
    if attempt_num == 1:
        context = "첫 번째 질문입니다. 상대방이 변명을 듣고 의심하거나 추가 설명을 요청합니다."
    elif attempt_num == 2:
        context = "두 번째 질문입니다. 상대방이 처음 질문과는 다른 각도에서 추가로 의심하거나 질문합니다."
    else:
        context = "세 번째(마지막) 질문입니다. 상대방이 최종 판단을 하기 위해 질문합니다. 자연스럽게 대화를 마무리하는 톤이어야 합니다."

    prompt = f"""당신은 상대방 역할을 하고 있습니다.

【 변명 】
"{excuse_text}"

【 맥락 】
{context}

【 요청 】
자연스럽고 현실적인 상대 대사를 생성하세요.
- 일상적인 대화체
- 변명의 내용에 기반한 구체적인 질문이나 반응
- 실제 상황에서 친구가 할 법한 말

【 응답 형식 】
{{"enemy_line": "상대방의 대사"}}

JSON만 반환하세요."""

    return prompt

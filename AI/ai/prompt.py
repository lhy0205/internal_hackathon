def generate_excuse_prompt(situation: str, politeness: int, credibility: int) -> str:
    """
    변명 생성 프롬프팅

    Args:
        situation: 상황 (예: "약속", "과제", "회식")
        politeness: 정중함 (1-10, 1=막말, 10=존댓말)
        credibility: 신뢰도 (1-10, 1=누가 봐도 뻥, 10=안 들킴)

    Returns:
        프롬프팅 문자열
    """

    if politeness >= 9:
        tone_politeness = "존댓말로 매우 정중하게. 정중한 사과와 배려 (예: 죄송합니다, 그러하겠습니다, 폐를 끼쳐드려 죄송합니다)"
    elif politeness >= 7:
        tone_politeness = "존댓말로 정중하게. 진심 어린 사과 (예: 죄송해요, 그렇게 하겠어요, 정말 죄송합니다)"
    elif politeness >= 5:
        tone_politeness = "존댓말로 보통. 기본적인 사과 (예: 미안해요, 죄송한데요, 그럼 다음에)"
    elif politeness >= 3:
        tone_politeness = "반말로 보통. 짧은 사과 (예: 미안해, 이번엔 못 가, 다음에 봐)"
    elif politeness >= 2:
        tone_politeness = "반말로 편함. 사과가 짧고 대충 (예: 미안, 뭐 어때, 다음에 봐, 그냥 못 가)"
    else:
        tone_politeness = "반말로 거칠게. 사과도 없고 당당하게 (예: 아 몰라, 나 못 가, 알아서 해, 그냥 싫어)"

    if credibility >= 10:
        tone_credibility = "완벽하고 구체적인 변명. 시간, 장소, 이유가 명확하고 논리적 일관성이 완벽해야 함. 실제 일어날 법한 상황. 등급은 S만 생성. 예: '어제 회의에서 내일 아침 프레젠테이션 준비하라고 지시받아서 자료 정리하는 중', '병원 진료받다가 추가 검사가 필요해서 오후 늦게까지 있어야 함'"
    elif credibility >= 9:
        tone_credibility = "거의 완벽한 변명. 구체적인 세부사항과 논리적 일관성. 등급은 S 2개 이상, A 최대 1개"
    elif credibility >= 7:
        tone_credibility = "자세히 보지 않으면 진짜인지 모를 수준. 등급은 A, B 위주로 생성"
    elif credibility >= 4:
        tone_credibility = "평범한 변명. 약간 의심스러움. **S와 A는 절대 금지**. 등급은 B, C만 생성해야 함"
    elif credibility >= 2:
        tone_credibility = "거짓말인 게 느껴지지만 그나마 그럴듯함. 등급은 F 위주로 생성"
    else:
        tone_credibility = "완전히 말이 안 되는 황당한 변명. 현실성이 전혀 없는 허무맹랑한 상황. 등급은 F만 생성. 예: '외계인이 나를 납치해서 못 나가', '요정이 우리 집에 와서 못 나가게 했어', '하늘에서 신이 나를 부르셨어'"

    prompt = f"""당신은 창의적인 변명 생성 AI입니다.

주어진 상황에 대해 그럴듯한 변명을 생성하세요.

【 상황 】
{situation}

【 톤 조절 】
정중함: {politeness}/10 ({tone_politeness})
신뢰도: {credibility}/10 ({tone_credibility})

【 중요한 지시사항 】
- 변명은 자연스럽고 일상적인 문장이어야 함 (어색하거나 부자연스러운 표현 금지)
- 정중함 레벨에 따라 사과의 방식이 달라져야 함:
  * 정중함 9~10: "정중하고 진심 어린 사과" (죄송합니다, 죄송해요)
  * 정중함 7~8: "기본적인 사과" (죄송해요, 미안해요)
  * 정중함 5~6: "간단한 사과" (미안해요)
  * 정중함 3~4: "짧은 사과" (미안해, 오늘은 못 가)
  * 정중함 2: "최소한의 사과" (뭐 어때, 다음에)
  * 정중함 1: "이유만 진술, 사과 없음, 극도로 단호한 톤" (예: "나 집에 있어야 해. 못 나가" - "그래서" 같은 연결사 사용 금지, 당당하게 끝내기)

【 신뢰도별 등급 생성 가이드 】
- 신뢰도 10: **S 등급 3개 모두** (절대!)
  * 매우 구체적인 세부사항 필수 (예: "회사에서 아침 9시 긴급 미팅 소집되어서 자료 준비 중")
  * 논리적 일관성이 완벽해야 함 (비합리적인 상황 절대 금지)
  * 실제로 일어날 법한 상황
  * 시간, 장소, 이유가 모두 명확해야 함
  * 문장이 자연스럽고 부자연스러운 표현 금지 (예: "갑자기 돌아버려서" ❌)

- 신뢰도 9: S 등급 2개 이상, A 등급 최대 1개
  * S급: 위와 동일하게 구체적이고 그럴듯함
  * A급: 자세히 보지 않으면 들키지 않음 (구체적인 상황, 일관성 있음, 일반인은 의심 못함)
    예: "엄마가 병원 가자고 해서 병원 다녀왔어", "학교 과제 발표 준비하느라 바빴어"

- 신뢰도 7~8: A 또는 B 등급만 (S 금지)
  * A급: 자세히 보지 않으면 들키지 않음 (구체적인 상황, 일관성 있음, 일반인은 의심 못함)
    예: "엄마가 병원 가자고 해서 병원 다녀왔어", "학교 과제 발표 준비하느라 바빴어"
  * B급: 설득력 있지만 물어보면 약해질 수 있음 (다소 구체적이지만 약한 부분 있음, 질문받으면 설명 필요)
    예: "형이랑 약속이 있었어", "누군가 나한테 물건을 맡겨놨어"

- 신뢰도 4~6: **B 또는 C 등급만** (S, A, F 절대 금지!)
  * B급: 설득력 있지만 물어보면 약해질 수 있음 (다소 구체적이지만 약한 부분 있음)
    예: "형이랑 약속이 있었어", "누군가 나한테 물건을 맡겨놨어"
  * C급: 약간의 의심이 있지만 그럴듯할 수 있음 (구체적이지 않은 이유, 쉽게 반박될 수 있는 부분 포함)
    예: "약속이 생겼어", "갑자기 일이 생겼어", "친구가 도움을 청했어"
  * 주의: 신뢰도 4~6에서 S나 A가 나오면 안 됨! 오직 B 또는 C만!

- 신뢰도 2~3: F 등급만 (높은 등급 금지)
  * F급: 논리적으로 말이 안 되거나 황당한 상황 (완전히 비현실적)
    예: "외계인이 나를 납치했어", "어제 요정을 봤어", "시간이 멈춰버렸어"

- 신뢰도 1: F 등급 3개 모두
  * 완전히 말이 안 되는 허무맹랑한 상황 (현실성 전혀 없음)
    예: "외계인이 나를 납치했어", "요정이 우리 집에 와서 못 나가게 했어", "하늘에서 신이 부르셨어"

【 요청 】
위의 상황과 톤에 맞게 다음을 JSON 형식으로 생성해주세요:

1. 변명 3개 (자연스럽고 일상적인 문장으로)
   - 신뢰도가 높을수록 구체적인 세부사항, 논리적 일관성, 실제 상황 같은 디테일 포함
   - 신뢰도 10: 매우 구체적 (시간, 장소, 이유 명확) + 논리적 완벽 + 실제 같은 상황
   - 신뢰도가 낮을수록 명백히 거짓 같은 느낌 (황당하거나 불가능한 상황)
   - **문장은 자연스럽고 매끄러워야 함 (어색한 표현 절대 금지)**
   - **"갑자기 돌아버려서", "뭔가 이상하게" 같은 부자연스러운 표현 금지**
   - 마치 실제 상황에서 상대에게 설명하는 것처럼 자연스러운 일상 대화체 사용
   - 불필요하게 복잡하거나 어려운 단어 금지
   - 위의 신뢰도별 가이드에 따라 정확한 등급으로만 생성할 것

2. 각 변명마다:
   - 예상되는 상대 반응 (상대의 입장에서 의심하거나 질문하는 형태로 "상대: 반응내용" 형식으로)
     * 상대는 변명을 완전히 믿지 말고 의심하거나 추가 설명을 요청해야 합니다
     * 반응은 변명의 구체적인 내용에 기반해야 하며, 이미 제시된 정보를 무시하고 다시 같은 질문하지 말 것
     * 문장은 자연스럽고 일상적인 대화체: 마치 실제 상황에서 친구나 지인이 하는 말처럼
     * 톤도 사용자의 정중함 레벨({politeness}/10)과 일치해야 함
     * 예: "정말? 어디가 안 좋아?", "증명할 수 있어?", "왜 미리 말 안 했어?"
   - 변명 등급 (S, A, B, C, F 중 하나) - 신뢰도 {credibility}에 맞게 선택할 것
     - S: 완벽한 변명, 누구도 의심할 수 없음 (신뢰도 9~10)
     - A: 거의 들키지 않음 (신뢰도 7~8)
     - B: 어느 정도 설득력 있음 (신뢰도 4~6)
     - C: 약간의 의심 (신뢰도 4~6)
     - F: 누가 봐도 황당한 거짓말 (신뢰도 1~3)
   - 유의사항 (이 변명을 사용할 때 주의할 점)

【 응답 형식 】
{{
    "excuses": [
        {{
            "text": "변명 내용",
            "reaction": "상대: 반응",
            "grade": "등급",
            "caution": "유의사항"
        }},
        {{
            "text": "변명 내용",
            "reaction": "상대: 반응",
            "grade": "등급",
            "caution": "유의사항"
        }},
        {{
            "text": "변명 내용",
            "reaction": "상대: 반응",
            "grade": "등급",
            "caution": "유의사항"
        }}
    ]
}}

【 CRITICAL - 반드시 확인 】
신뢰도 {credibility}에 맞는 등급으로만 생성했는가?
- 신뢰도 10: 위의 3개 변명 등급이 모두 "S"인가? (S가 아니면 실패! 다시 생성하세요!)
  * 3개 모두 S 등급이 맞는지 다시 한 번 확인!
- 신뢰도 9: 위의 3개 변명 중 최소 2개 이상이 "S"인가? (A가 최대 1개만 가능)
- 신뢰도 7~8: 위의 3개 변명이 "A" 또는 "B"만인가? (S 없음)
- 신뢰도 4~6: 위의 3개 변명이 "B" 또는 "C"만인가? (S와 A 절대 없음!)
- 신뢰도 2~3: 위의 3개 변명이 "F"만인가?
- 신뢰도 1: 위의 3개 변명이 모두 "F"인가?

또한 모든 문장이 자연스럽고 부자연스러운 표현이 없는지 확인하세요!

JSON만 반환하세요. 설명이나 다른 텍스트는 포함하지 마세요."""

    return prompt


def generate_defense_reaction_prompt(
    original_excuse: str, opponent_reaction: str, user_defense: str, attempt_num: int
) -> str:
    """
    사용자의 디펜스 말에 대한 상대의 반응 생성

    Args:
        original_excuse: 원래 변명
        opponent_reaction: 상대의 초기 반응
        user_defense: 사용자의 디펜스 말
        attempt_num: 시도 횟수 (1-3)

    Returns:
        프롬프팅 문자열
    """
    if attempt_num == 1:
        context = "첫 번째 설명입니다. 사용자의 답변이 구체적이고 그럴듯하면 일부 설득될 여지가 있습니다."
    elif attempt_num == 2:
        context = "두 번째 설명입니다. 사용자가 이미 한 번 설명했으므로, 다시 같은 질문을 하지 마세요. 추가 정보가 있는지 확인하거나, 사용자의 답변을 받아들이는 방향으로 진행하세요."
    else:
        context = "세 번째(마지막) 설명입니다. 사용자가 이미 여러 번 설명했으므로 반복 질문을 하지 마세요. 사용자의 설명을 수용하거나, 다른 각도의 질문을 하되 자연스럽게 대화를 마무리하는 방향으로 진행하세요."

    prompt = f"""당신은 현실적인 상대방 역할을 하고 있습니다.

【 상황 】
당신은 상대방입니다. 사용자가 처음에 이렇게 변명했습니다:
"{original_excuse}"

【 당신의 초기 반응 】
"{opponent_reaction}"

【 사용자의 새로운 답변 】
"{user_defense}"

【 맥락 】
{context}

【 요청 】
사용자의 답변을 듣고 자연스러운 반응을 생성하세요.
- 사용자의 답변의 구체성과 신뢰성을 고려하세요
- 좋은 답변이면 점진적으로 설득되세요
- 부족한 답변이면 의심하거나 새로운 각도의 질문을 해주세요 (이미 묻던 것 반복 금지)
- 이미 사용자가 제공한 정보에 대해 다시 묻지 마세요
- 상대의 입장에서 자연스럽고 현실적인 반응을 하세요
- 문장이 자연스럽게 흘러가도록: 어색한 표현 금지, 일상 대화체 사용

JSON 형식: {{"reaction": "상대방의 반응"}}"""

    return prompt


def measure_suspicion_prompt(
    original_excuse: str, conversation_history: list
) -> str:
    """
    전체 대화를 보고 의심도 측정

    Args:
        original_excuse: 원래 변명
        conversation_history: 대화 기록 (각 항목: {"user": "...", "opponent": "..."})

    Returns:
        프롬프팅 문자열
    """
    conversation_text = "\n".join(
        [
            f"사용자: {turn['user']}\n상대: {turn['opponent']}"
            for turn in conversation_history
        ]
    )

    prompt = f"""당신은 심리 분석가입니다. 주어진 대화를 분석하여 상대방의 의심도를 측정하세요.

【 원래 변명 】
"{original_excuse}"

【 대화 기록 】
{conversation_text}

【 평가 기준 】
다음 요소들을 종합적으로 고려하여 의심도를 판단하세요:

0. 원래 변명 자체의 현실성 (가장 중요!)
   - 변명이 현실적이고 가능한가? (예: 친구 약속, 몸 안 좋음) → 대화에 따라 의심도 조정
   - 아니면 비현실적이고 황당한가? (예: 요정, 마법, 피카소 그림, 우주적 현상) → 의심도는 거의 100%에 고정
   - 비현실적인 변명의 의심도는 절대 80% 이하로 내려가면 안 됨. 95~100% 범위 유지

1. 사용자의 답변 품질 (매우 중요!)
   - 구체적이고 상세한가? (구체적 설명이 있으면 의심도 큰 폭으로 감소)
   - 신뢰할 수 있는가?
   - 일관성이 있는가?
   - 추가 정보나 설명을 제공했는가? (적극적으로 설명하면 의심도 감소)
   - 회피하지 않고 진심 어린 답변을 했는가? (진심이 느껴지면 의심도 감소)

2. 상대방의 반응
   - 점진적으로 설득되고 있는가?
   - 여전히 의심하고 있는가?
   - 질문이나 요구가 있는가?

3. 누적 효과
   - 여러 번의 설득 시도를 고려하세요
   - 현실적인 변명: 의심도가 점진적으로 낮아질 수 있음
   - 비현실적인 변명: 아무리 설득해도 의심도는 95~100% 범위에서만 유지 (절대 80% 이하로 내려가면 안 됨)

【 의심도 해석 】
- 0%: 완전히 믿음
- 25~40%: 대부분 믿음
- 40~60%: 반반 정도 의심
- 60~80%: 상당히 의심
- 100%: 완벽히 의심함

【 요청 】
위 기준을 바탕으로 현재 상대방의 의심도를 판단하세요.

JSON 형식: {{"suspicion": 숫자(0-100), "reason": "의심도 판단 이유"}}"""

    return prompt


def analyze_defense_type_prompt(conversation_history: list) -> str:
    """
    디펜스 유형 분석

    Args:
        conversation_history: 대화 기록 (각 항목: {"user": "...", "opponent": "..."})

    Returns:
        프롬프팅 문자열
    """
    conversation_text = "\n".join(
        [
            f"사용자: {turn['user']}\n상대: {turn['opponent']}"
            for turn in conversation_history
        ]
    )

    prompt = f"""당신은 심리 분석가입니다. 주어진 대화를 상세히 분석하여 사용자의 디펜스 전략을 파악하고, 가장 적합한 유형을 분석하세요.

【 대화 기록 】
{conversation_text}

【 참고: 일반적인 디펜스 유형 예시 】
다음은 참고만 하세요. AI가 유연하게 판단하되, 유형명은 창의적으로 지을 수 있습니다:

1. 감성호소형: 감정에 호소하며 상대의 공감을 유도
   - 특징: 힘들다, 미안하다, 걱정된다 등 감정 표현 중심
   - 예: "진짜 힘들어서", "너무 미안해요", "이해해줄 수 있을까?"

2. 논리전개형: 구체적인 이유와 상황을 체계적으로 설명
   - 특징: 구체적 사실, 시간, 상황, 상세한 설명
   - 예: "어제부터 몸이 안 좋았어요. 응급실에 가야 합니다. 검사를..."

3. 냅다사과형: 빠르게 사과하고 대안 제시하며 마무리
   - 특징: 사과 후 즉시 다음 날짜나 대안 제시
   - 예: "미안해, 다음에 꼭", "죄송합니다, 내일은 가겠습니다"

4. 우물쭈물형: 구체적 답변을 피하거나 모호하게 대응
   - 특징: 구체적 설명 회피, 모호한 표현
   - 예: "글쎄...", "그럴 수도 있지", "아마도..."

5. 윈윈제안형: 대안이나 타협점을 제시하며 설득
   - 특징: "대신에...", "이렇게 하면?", "절충하자"
   - 예: "대신 내일 처리할게", "대체 언제면 괜찮을까?"

6. 너도그럴수있잖아형: 자신의 행동/주장을 합리화하며 자기편 만들기
   - 특징: "너도 그럴 수 있잖아", "누구나 이런 경험 있어"
   - 예: "이건 누구나 그럴 수 있는 상황이야", "너도 이런 적 있잖아"

【 분석 방법 】
1. 사용자의 답변을 읽고, 주로 어떤 방식으로 설득하려고 하는지 파악하세요
2. 감정 표현의 정도, 구체적 설명의 양, 사과의 빈도, 대안 제시 유무 등을 고려하세요
3. 여러 유형이 섞여있다면, 가장 지배적인 전략을 선택하세요
4. 위 유형들이 정확히 맞지 않으면, 실제 대화의 특징을 재밌게 표현하는 새로운 유형명을 만들어도 됩니다
   (예: "감성논리혼합형", "도피후반격형", "죄송이거하나형" 등 창의적인 이름 가능)

【 요청 】
사용자의 전체 디펜스 대화를 상세히 분석하고, 실제 대화에 가장 잘 맞는 유형을 판단하세요.

JSON 형식: {{"defense_type": "가장 적합한 유형명", "reason": "이 유형으로 판단한 이유 (어떤 말과 행동이 이 유형의 특징을 보였는지 구체적으로)"}}"""

    return prompt


def generate_caution_prompt(excuses: list) -> str:
    """
    변명별 유의사항 생성

    Args:
        excuses: 변명 목록 (각 항목: {"text": "변명 내용", ...})

    Returns:
        프롬프팅 문자열
    """
    excuses_text = "\n".join(
        [f"{i+1}. {excuse['text']}" for i, excuse in enumerate(excuses)]
    )

    prompt = f"""당신은 실생활 조언 전문가입니다.

【 변명 목록 】
{excuses_text}

【 요청 】
각 변명을 실행할 때 들킬 수 있는 상황을 피하기 위한 구체적인 행동 유의사항을 생성하세요.

각 변명마다:
- 피해야 할 행동들
- 일관성 유지 방법
- SNS/통신 주의사항
- 추가 증거 준비 필요시 언급

예시:
변명: "몸이 안 좋아서 집에 있어"
유의사항: "인스타그램 스토리에 활동 내용 올리지 말기, 친구 전화는 조용한 환경에서 받기, 병원 영수증 준비"

【 응답 형식 】
{{
    "cautions": [
        {{
            "excuse": "변명 내용",
            "cautions": ["주의사항1", "주의사항2", "주의사항3"]
        }},
        ...
    ]
}}

JSON만 반환하세요."""

    return prompt


def generate_preview_prompt(tone: int, risk: int) -> str:
    """
    미리보기용 한 줄 변명 생성

    Args:
        tone: 정중함 (1-10)
        risk: 신뢰도 (1-10)

    Returns:
        프롬프팅 문자열
    """
    if tone >= 9:
        tone_text = "존댓말로 매우 정중하게"
    elif tone >= 7:
        tone_text = "존댓말로 정중하게"
    elif tone >= 5:
        tone_text = "존댓말로 보통"
    elif tone >= 3:
        tone_text = "반말로 보통"
    elif tone >= 2:
        tone_text = "반말로 편하게"
    else:
        tone_text = "반말로 거칠게"

    if risk >= 9:
        risk_text = "완벽한 변명 (S급)"
    elif risk >= 7:
        risk_text = "거의 들키지 않는 변명 (A급)"
    elif risk >= 4:
        risk_text = "어느 정도 설득력 있는 변명 (B-C급)"
    else:
        risk_text = "거짓말 같은 변명 (F급)"

    prompt = f"""당신은 창의적인 변명 생성 AI입니다.

【 요청 】
한 줄짜리 짧은 변명을 생성하세요.

【 조건 】
- 톤: {tone_text} ({tone}/10)
- 신뢰도: {risk_text} ({risk}/10)
- 자연스럽고 일상적인 표현
- 한 문장으로 간결하게 (20~40자 정도)

【 응답 형식 】
{{"text": "한 줄 변명", "grade": "등급"}}

grade는 S, A, B, C, F 중 하나 (신뢰도 {risk}에 맞게)

JSON만 반환하세요."""

    return prompt


